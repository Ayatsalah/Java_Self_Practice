package day02_variables;

public class Birthaday {
    public static void main(String[] args) {
        String name = "John";
        int birthDay = 25;
        String birthMonth = "April";
        int birthYear = 1995;
        System.out.println(name + " was born on " + birthMonth + " " + birthDay + "," + " " + birthYear + ".");
    }
}
//			John was born on April 25, 1995.
